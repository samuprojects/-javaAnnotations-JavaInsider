package theory;

@SuppressWarnings("ALL")
public class ExposingAnnotations {

    /*
    *
    * O Java não mostra na documentação quais annotations estão em utilização, mesmo no javadoc
    * Porém, as vezes pode ser importante em algum cenário que essa informação aparecça.
    * Para isso na annotation que se deseja expor (no nosso exemplo é a @Id) incluímos a meta annotation @Documented
    * Se passar o mouse sobre o getId na classe executável o @Id estará visível bem como na geração do javadoc
    *
    *
    *
    * */
}
